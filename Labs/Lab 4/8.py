s=int(input('Введите сумму покупки: '))
if s<1000:
    print('Ваша скидка: 0% ')
    print('К оплате: ',s)
if 1000<=s<5000:
    print('Ваша скидка: 5% ')
    print('К оплате: ',s-((s*5)/100))
if 5000<=s<=10000:
    print('Ваша скидка: 10% ')
    print('К оплате: ',s-((s*10)/100))
if s>10000:
    print('Ваша скидка: 15% ')
    print('К оплате: ',s-((s*15)/100))